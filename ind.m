function S = ind(x,y)
[b,a] = butter(4,[5/256 30/256],'bandpass');
x = filter(b,a,x);
x=x-mean(x(:));
x=x/std(x(:));
y=y-mean(y(:));
y=y/std(y(:));
[n,m]=size(x);
k=(1/m);
c12=k*x*y';
c21=c12';
c11=k*x*x';
c22=k*y*y';
C=[c11 c12;c21 c22];
C11=c11^-0.5;
C22=c22^-0.5;
[a b]=size(C22);
U=zeros(b+1,b+1);
U(1,1)=C11;
U(2:b+1,2:b+1)=C22;
R=U*C*U';
% R12=C11*c12*C22;
% R21=C22*c21*C11;
e=eig(R);
E=e/sum(e);%normalised eigen value
S=1+(sum(E.*log(E)))/log(7);
